# 4. Check Even or Odd
# Write a function is_even(n) that returns True if the number is even, otherwise False.
def is_even(n):
    return n % 2 == 0
# Test the function
result = is_even(4) 
print("Is 4 even?", result)  # Output: Is 4 even? True
result = is_even(5)
print("Is 5 even?", result)  # Output: Is 5 even? False


